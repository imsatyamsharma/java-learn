class Areaofcircle
{
	public static void main(String args[])
	{
		//variable declare
		int radius;
		double pi,result;
		//put the values
		pi=3.14;
		radius=2;
		//put the formula
		result=pi*radius*radius;
		//print the value 
		System.out.println("Value of pi ="+pi);
		System.out.println("Value of radius ="+radius);
		System.out.println("Areaofcircle ="+result);


	}
}