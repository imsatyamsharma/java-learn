class Incdec
{
	public static void main(String args[])
	{
		int b;
		b=40;
		System.out.println(b);
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(b--);
		System.out.println(--b);
		System.out.println(b++);
		System.out.println(--b);
		System.out.println(b++);
		System.out.println(--b);
	}
}