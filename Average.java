class Average
{
	public static void main(String args[])
	{
		//variable declare
		int a,b,c,d;
		//put the values
		a=20;
		b=40;
		c=60;
		//put the formula
		d=(a+b+c)/3;
		//print the value
		System.out.println("Value of a ="+a);
		System.out.println("Value of b ="+b);
		System.out.println("Value of c ="+c);
		System.out.println("Average of a,b and c ="+d);

	}
}